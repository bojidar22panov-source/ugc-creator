/**
 * Applies stress marks to Bulgarian text using the dictionary
 * @param text - The Bulgarian text to process
 * @returns Text with stress marks applied
 */
export function applyStressMarks(text: string): string {
    if (!text) return text

    // Split text into words while preserving punctuation and whitespace
    const words = text.split(/(\s+|[.,!?;:'"()\-–—])/g)

    const processedWords = words.map(word => {
        // Skip whitespace and punctuation
        if (/^\s*$/.test(word) || /^[.,!?;:'"()\-–—]+$/.test(word)) {
            return word
        }

        // Convert to lowercase for dictionary lookup
        const lowerWord = word.toLowerCase()

        // Check if word exists in dictionary
        if (STRESS_DICTIONARY[lowerWord]) {
            const stressedWord = STRESS_DICTIONARY[lowerWord]

            // Preserve original capitalization
            if (word[0] === word[0].toUpperCase()) {
                // Capitalize first letter of stressed word (after any stress marks)
                return capitalizeFirstLetter(stressedWord)
            }

            return stressedWord
        }

        // Word not in dictionary - return as is
        // Could apply default stress rules here for unknown words
        return word
    })

    return processedWords.join('')
}

/**
 * Capitalizes the first actual letter in a string (skipping stress marks)
 */
function capitalizeFirstLetter(str: string): string {
    let firstLetterIndex = -1

    for (let i = 0; i < str.length; i++) {
        if (/[а-яА-Яa-zA-Z]/.test(str[i])) {
            firstLetterIndex = i
            break
        }
    }

    if (firstLetterIndex === -1) return str

    return (
        str.substring(0, firstLetterIndex) +
        str[firstLetterIndex].toUpperCase() +
        str.substring(firstLetterIndex + 1)
    )
}

/**
 * Removes stress marks from text (for display purposes)
 */
export function removeStressMarks(text: string): string {
    return text.replace(/[ˈˌ.]/g, '')
}

